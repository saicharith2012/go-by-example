package main

import "fmt"

func main() {

	// golang has various value types. 
	// strings
	// integers
	// floats
	// booleans etc...

	fmt.Println("maro" + "prapancham") // string concatenation.
	fmt.Println("4 + 5 = ", 4+5)
	fmt.Println("7.0/3.0 = ", 7.0/3.0) // floating point division

	fmt.Println(true && false) // AND
	fmt.Println(true || false) // OR
	fmt.Println(!true) // NOT

}
